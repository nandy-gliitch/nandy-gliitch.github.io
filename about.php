 HABITANT MIGRATION is the movement of people from one place in the world to another.<br> <br><br>
 Human patterns of movement reflect the conditions of a changing world and impact the cultural<br>
 landscapes of both the places people leave and the places they settle.<br>
 vocabulary:<br>

emigrant: person who moves from their existing country or region to a new country or region.<br><br><br>
emigration: process of leaving one country or region to live in another.<br><br><br>
external migration: the movement of people to another nation or country.<br><br><br>

human migration: the movement of people from one place to another.<br><br><br>

immigrant: person who moves to a new country or region.<br><br><br>

immigration: process of moving to a new country or region with the intention of staying and living there.<br><br><br>

internal migration: the movement of people from one area in a country or nation to another.<br><br><br>

pull factor: force that draws people to immigrate to a place.<br><br><br>

push factor: force that drives people away from a place.<br><br><br>

refugee: person who flees their home, usually due to natural disaster or political upheaval.<br><br><br>

return migration: the return of immigrants to their home country.<br><br><br>
seasonal migration: movement of animals or other organisms determined by the changing weather or seasons, or in response to labor or climate conditions. <br><br>
For animals, seasonal migration usually refers to movement to a warmer climate during the winter and a cooler climate during the summer. <br><br><br>
For humans, seasonal migration may happen because of drivers such as crop and livestock management or tourism.<br><br><br>

